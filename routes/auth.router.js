const express = require('express');
const router = express.Router();
const passport = require('passport');
const AuthService = require('../services/auth.service');
const service = new AuthService();
const validatorHandler = require('./../middlewares/validator.handler');
const {
  recoveryEmailSchema,
  resetPasswordSchema,
} = require('../schemas/auth.schema');

router.post(
  '/login',
  passport.authenticate('local', { session: false }),
  async (req, res, next) => {
    try {
      const user = req.user;
      res.json(service.genToken(user));
    } catch (error) {
      next(error);
    }
  }
);

router.post(
  '/recovery',
  validatorHandler(recoveryEmailSchema, 'body'),
  async (req, res, next) => {
    try {
      const email = req.body.email;
      const rta = await service.sendRecoveryMail(email);
      res.json(rta);
    } catch (error) {
      next(error);
    }
  }
);

router.post(
  '/recover-password',
  validatorHandler(resetPasswordSchema, 'body'),
  async (req, res, next) => {
    try {
      const { password, token } = req.body;
      const rta = await service.resetPassword(token, password);
      res.json(rta);
    } catch (error) {
      next(error);
    }
  }
);

module.exports = router;
